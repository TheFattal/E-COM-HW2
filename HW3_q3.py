# Input two strings:
str1 = input("Enter the first string: ")
str2 = input("Enter the second string: ")

# Concatenate the strings with '*' in between
result1 = f"*{str1}*{str2}*"
result2 = f"-{str1}-{str2}-"

# Print the result
print("The * result is:", result1)
print("The - result is:", result2)

